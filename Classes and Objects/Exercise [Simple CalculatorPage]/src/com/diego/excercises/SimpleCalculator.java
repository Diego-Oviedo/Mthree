package com.diego.excercises;

public class SimpleCalculator {

	
	public int addition(int x , int y) {
		
		int result = (x + y);
		
		return result;
	}
	
	public int subtraction(int x , int y) {
		
		int result = (x - y);
		
		return result;
	}	
	
	public int multiplication(int x , int y) {
		
		int result = (x * y);
		
		return result;
	}
	
	public int division(int x , int y) {
		
		int result = (x / y);
		
		return result;
	}	
	
}
